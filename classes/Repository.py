from abc import ABC,abstractmethod

#Class for handling user storage
class IAccountRepo(ABC):

    @abstractmethod
    def createUser():
        raise NotImplemented
    
    @abstractmethod
    def updateUser():
        raise NotImplemented
    
    @abstractmethod
    def deleteUser():
        raise NotImplemented
    
    @abstractmethod
    def getUsers():
        raise NotImplemented
    
    


